use crate::wasm4::{ self };
use crate::palette::set_draw_color;
//---Explosion Properties---//
// WIDTH= 8;
// HEIGHT= 8;
// FLAGS= 0; // BLIT_1BPP

const EXPLO1: [u8; 8] = [0x00, 0x00, 0x00, 0x18, 0x18, 0x00, 0x00, 0x00];
const EXPLO2: [u8; 8] = [0x00, 0x00, 0x24, 0x18, 0x18, 0x24, 0x00, 0x00];
const EXPLO3: [u8; 8] = [0x00, 0x20, 0x12, 0x1c, 0x38, 0x48, 0x04, 0x00];
const EXPLO4: [u8; 8] = [0x20, 0x10, 0x09, 0x22, 0x44, 0x90, 0x08, 0x04];
const EXPLO5: [u8; 8] = [0x08, 0x04, 0x40, 0x80, 0x01, 0x02, 0x20, 0x10];
const EXPLO6: [u8; 8] = [0x40, 0x01, 0x00, 0x00, 0x00, 0x00, 0x80, 0x02];

pub struct Explode {
    xpos: i32,
    ypos: i32,
}

impl Explode {
    pub fn new(xpos: i32, ypos: i32) -> Self {
        Self {
            xpos: xpos,
            ypos: ypos,
        }
    }

    pub fn explode(&self) {
        set_draw_color(0x0002);
        wasm4::blit(&EXPLO1, 20, 20, 8, 8, 0);
    }
}
